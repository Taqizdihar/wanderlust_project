                          dalam kondisi di alam bebas
                          ketika berkemah. Setiap ilmu
                          yang dipelajari pasti bisa
                          menjadi bermanfaat.
                        </p>
                        <h5>4. Bersama Teman</h5>
                        <p>
                          Ketika akan melakukan kemah,
                          sangat disarankan untuk selalu
                          mengajak teman karena segala
                          sesuatu akan terasa lebih
                          menantang di alam! Pastikan
                          teman Anda juga senang
                          berkemah, ya.
                        </p>
